Chapter 1 - PHP Basics

Every PHP application is forged from a small set of basic construction blocks.

From its very inception, PHP was built with the intent of providing simplicity and choice — and this is clearly reflected in the number of options available in building applications.

In this chapter, we will cover the essentials that you will use day in and day out.

