2. Declaring a Class

The basic declaration(1) of a class is very simple:

<?php
class myClass {
	// Class contents go here
}
?>

As you have probably(2) guessed, this advises(3) the PHP interpreter(4) that
you are declaring a class called myClass whose contents will normally be a combination(5) of
constants(6), variables and functions (called methods).

