OOP revolves around the concept of grouping code and data together in logical units called classes.
This process is usually referred to as encapsulation, or information hiding, since its goal is that of dividing an application into separate entities whose internal components can change without altering their external interfaces.
OOP xoay quanh khái niệm của việc nhóm code và data vào với nhau theo đơn vị logic gọi là các class.
Quá trình này thường được gọi là đóng gói, hoặc ẩn thông tin, vì mục tiêu của nó là chia ứng dụng thành các entities riêng biệt có các component bên trong có thể thay đổi mà không thay đổi các interface bên ngoài của chúng.
