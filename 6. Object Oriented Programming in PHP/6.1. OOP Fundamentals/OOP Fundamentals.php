Thus, classes are essentially a representation of a set of functions (also called methods) and variables (called properties) designed to work together and to provide a specific interface
to the outside world.
It is important to understand that classes are just blueprints that cannot be used directly — they must be instantiated into objects, which can then interact with the rest of the application.
You can think of classes as the blueprints for building a car, while objects are, in fact, the cars themselves as they come out of the production line.
Just like a single set of blueprints can be used to produce an arbitrary number of cars, an individual class can normally be instantiated into an arbitrary number of objects.

Như vậy, các class về cơ bản là một đại diện của một tập các function (còn gọi là các method) và các variable (được gọi là các property) được thiết kế để làm việc cùng nhau và cung cấp một interface cụ thể cho thế giới bên ngoài.
Điều quan trọng là phải hiểu rằng các class chỉ là các bản thiết kế không thể được sử dụng trực tiếp - 
chúng phải được khởi tạo thành các object, sau đó có thể tương tác với phần còn lại của ứng dụng.
Bạn có thể nghĩ các class như là bản thiết kế để tạo 1 car, trong khi các object, trên thực tế, chính là các car khi ra khỏi xây chuyền sản xuất.
Cũng giống như một bộ các bản thiết kế có thể được sử dụng để tạo ra một số lượng car tùy ý, một class riêng thường có thể được khởi tạo vào một số lượng các object tùy ý.
